    private void DeletaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeletaMouseClicked
        int id = Integer.parseInt(jTextFieldId.getText());
        
        controle.removeCliente(id);
        listarTab(); 
    }//GEN-LAST:event_DeletaMouseClicked

    private void BuscaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BuscaMouseClicked
        int id = Integer.parseInt(jTextFieldId.getText());
        
        Cliente i = controle.buscarCliente(id);
        
        DefaultTableModel modelo = (DefaultTableModel) tblInst.getModel();
        
        int posLin = 0;
        modelo.setRowCount(posLin);
                
        modelo.insertRow(posLin, new Object[]{i.getId(), i.getNome(), i.getIdade(), i.getEmail()});
    }//GEN-LAST:event_BuscaMouseClicked

    private void EditaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditaMouseClicked
        if(jTextFieldIdade.getText().equals("")){
            controle.setCliente(
            jTextFieldNome.getText(),
            0,
            jTextFieldEmail.getText());
        } else {
            controle.setCliente(
            jTextFieldNome.getText(),
            Integer.valueOf(jTextFieldIdade.getText()),
            jTextFieldEmail.getText());
        }
        
        controle.editarCliente(Integer.parseInt(jTextFieldId.getText()));
        listarTab(); 
    }//GEN-LAST:event_EditaMouseClicked

