    public void listarTab(){
        DefaultTableModel modelo = (DefaultTableModel) tblInst.getModel();
        
        int posLin = 0;
        modelo.setRowCount(posLin);
                
        for(Cliente i: controle.getClientes()){
            modelo.insertRow(posLin, new Object[]{i.getId(), i.getNome(), i.getIdade(), i.getEmail()});
            posLin++;
        }
    }

